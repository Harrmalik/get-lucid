      <h2 class="ui header">Exploring The Lucid Dream World</h2>
      <p>At first, I was stunned by the realism of my dream reality. For the first time in my life,
       I was awake and aware in a completely separate reality. It was like a science-fiction movie.
       I couldn't get my head around it.</p>

      <p>At the same time, I was overjoyed by the realization that I could control and direct my awareness
       within my supersensory dreamstate. Anything was possible.</p>

      <p>I practiced more advanced lucid dreaming induction techniques so that I could have more of these
       dreams and explore further into the depths of my mind.</p>

      <p>Sometimes I can lucid dream every night, or even multiple times per night. It can be as easy telling
       myself to lucid dream as I go to sleep. It'll just happen spontaneously.</p>

      <p>In all my lucid dreams, I know I am dreaming and I increase the intensity of the dream at will.
      I can 'turn up' my level of consciousness, allowing me to see, hear, feel, taste and smell everything
      as vividly as waking life.</p>

      <p>I can create my own dramatic scenery, teleport to anywhere in the universe, interact with dream figures
       and speak directly to my unconscious mind.</p>

      <p>This is where I start to get lost for words. Which sucks as a writer. Because no matter what I say here,
       I can't convey the beauty and emotion of a truly inspired lucid dream. I'll just start to sound fluffy
       and hippy. But the feelings are real - and it's extraordinary.</p>

      <p>A great lucid dream can change the way you view the waking world. Imagine then, summoning a lifetime of
      lucid dreams for yourself...</p>
